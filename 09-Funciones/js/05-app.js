function sumar (a, b) { // a y b son parametros (valores representativos)
    console.log(a + b);
};
sumar(2, 3); // 2 y 3 son argumentos (valores reales)
sumar(200, 500)


function saludar (nombre, apellido) {
    console.log(`Hola ${nombre} ${apellido}`);
};
